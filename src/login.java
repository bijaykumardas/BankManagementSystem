private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
// TODO add your handling code here:
    String card = cardnumber.getText();
    String pin = pinnumber.getText();
    try {
        Connection con = (Connection) DriverManager.getConnection("jdbc:mysql://localhost:3306/bankmangementsystem", "root", "");
        String query = "select * from logincred where card_no='" + card + "' and PIN='" + pin + "'";
        Statement sta = (Statement) con.createStatement();
        ResultSet rs = sta.executeQuery(query);
        if (rs.next()) {
            setVisible(false);
            Transections t = new Transections();
            t.setData(pin);
            t.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(null, "Incorrect Card Number or PIN");
        }
    } catch (Exception ex) {
        System.out.print(ex);
    }

}//GEN-LAST:event_jButton1ActionPerformed

