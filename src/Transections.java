private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
// TODO add your handling code here:
    int balance = 0;
    try{
            Connection connection = (Connection) DriverManager.getConnection("jdbc:mysql://localhost:3306/bankmangementsystem", "root", "");
            String query = "select * from transection where pin = '"+pinNumber+"'";
            Statement sta = (Statement) connection.createStatement();
            ResultSet rs = sta.executeQuery(query);
            while (rs.next()) {
                if (rs.getString("type").equals("Deposit")) {
                    balance += Integer.parseInt(rs.getString("amount"));
                } else {
                    balance -= Integer.parseInt(rs.getString("amount"));
                }
            }
        }catch(Exception e){
          System.out.print(e);  
        }
    bal=Integer.toString(balance);
    Balance b =  new Balance();
    b.setData(pinNumber, bal);
    b.setVisible(true);
    dispose();
}//GEN-LAST:event_jButton6ActionPerformed

